# Addition +\
print(3 + 10)
# Subtraction -
print (10 - 3)
# Multiplication *
print (3*3)
# Division /
print (11/2)
print (int(11/2))
# Modulus %
print (9 % 2)
print (-10 % 3)
# Exponentiation **
print (3**3)
# Floor division //       N = D * (N // D) + (N % D) (phép chia lấy phần nguyên)
print (10 // 3)     # floor division: nhận được số nguyên lớn nhất nhỏ hơn hoặc bằng kết quả

