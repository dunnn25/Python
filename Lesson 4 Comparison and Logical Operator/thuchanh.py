# # Comparison operators
# print(20 == 20)
print("Anh" == "em")

# # not equal
print(20 != 10)
print("anh" != "em")

# # >, >=, <, <=
print(10 > 10)

# logical operator
num = 15
print("Number is in the range [10, 20]: ", num >= 10 and num <= 20)
print("Number is in the range [10, 20]: ", 10 <= num <= 20)
bol = False
print(not bol)
print(not (6 > 5))
